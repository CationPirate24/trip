package com.java.vo;

public class MyInterestVo {

	private String id;
	private String name;
	private String price;
	private String describe;
	private String pictureId;
	private String mapId;
	private String grade;
	private String type;
	private String myCityId;
	private String address;
	
	private String typeVo;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getDescribe() {
		return describe;
	}

	public void setDescribe(String describe) {
		this.describe = describe;
	}

	public String getPictureId() {
		return pictureId;
	}

	public void setPictureId(String pictureId) {
		this.pictureId = pictureId;
	}

	public String getMapId() {
		return mapId;
	}

	public void setMapId(String mapId) {
		this.mapId = mapId;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getMyCityId() {
		return myCityId;
	}

	public void setMyCityId(String myCityId) {
		this.myCityId = myCityId;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getTypeVo() {
		return typeVo;
	}

	public void setTypeVo(String typeVo) {
		this.typeVo = typeVo;
	}
	
}
